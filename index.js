function add(a,b){
  return a+b
}

function subtract(a,b){
  return a-b
}

function multiply (a,b){
  return a*b
}

function divide(a,b){
  return a/b
}

function increment(a){
  a = a+=1
  return a
}

function decrement(a){
  a = a-=1
  return a
}

function makeInt(a,base){
  parseInt(a,base)
  return a
}
